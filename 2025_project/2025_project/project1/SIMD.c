// AVX2循环左移操作
static inline __m256i avx2_rotate_left_32(__m256i input_vector, int shift_bits) {
    return _mm256_or_si256(_mm256_slli_epi32(input_vector, shift_bits),
        _mm256_srli_epi32(input_vector, 32 - shift_bits));
}

// AVX2线性变换L
static inline __m256i avx2_linear_transform_l(__m256i input_vector) {
    return _mm256_xor_si256(
        _mm256_xor_si256(
            _mm256_xor_si256(
                _mm256_xor_si256(input_vector, avx2_rotate_left_32(input_vector, 2)),
                avx2_rotate_left_32(input_vector, 10)),
            avx2_rotate_left_32(input_vector, 18)),
        avx2_rotate_left_32(input_vector, 24));
}

// S-box替换（AVX2模拟实现）
static __m256i avx2_byte_substitution(__m256i input_vector) {
   // alignas(32);
    uint8_t buffer_array[32];
    _mm256_store_si256((__m256i*)buffer_array, input_vector);

    for (int i = 0; i < 32; i++) {
        buffer_array[i] = SBOX[buffer_array[i]];
    }

    return _mm256_load_si256((__m256i*)buffer_array);
}

// AVX2复合变换T
static inline __m256i avx2_composite_transform_t(__m256i input_vector) {
    return avx2_linear_transform_l(avx2_byte_substitution(input_vector));
}

// 并行加密4个数据块
void sm4_encrypt_four_blocks_parallel(const uint8_t input_blocks[4][16], uint8_t output_blocks[4][16], const uint32_t round_keys[32]) {
    // 状态向量，每个__m256i包含4个不同位置的相同字节
    __m256i state_vector_0, state_vector_1, state_vector_2, state_vector_3;

    // 加载数据
    uint32_t temp[16];
    for (int i = 0; i < 4; i++) {
        memcpy(&temp[i * 4], input_blocks[i], 16);
    }

    state_vector_0 = _mm256_set_epi32(temp[12], temp[8], temp[4], temp[0],
        temp[12], temp[8], temp[4], temp[0]);
    state_vector_1 = _mm256_set_epi32(temp[13], temp[9], temp[5], temp[1],
        temp[13], temp[9], temp[5], temp[1]);
    state_vector_2 = _mm256_set_epi32(temp[14], temp[10], temp[6], temp[2],
        temp[14], temp[10], temp[6], temp[2]);
    state_vector_3 = _mm256_set_epi32(temp[15], temp[11], temp[7], temp[3],
        temp[15], temp[11], temp[7], temp[3]);

    // 32轮迭代
    for (int round = 0; round < 32; round++) {
        __m256i round_key_vector = _mm256_set1_epi32(round_keys[round]);
        __m256i temp_vector = _mm256_xor_si256(_mm256_xor_si256(state_vector_1, state_vector_2), state_vector_3);
        temp_vector = _mm256_xor_si256(temp_vector, round_key_vector);
        temp_vector = _mm256_xor_si256(state_vector_0, avx2_composite_transform_t(temp_vector));

        state_vector_0 = state_vector_1;
        state_vector_1 = state_vector_2;
        state_vector_2 = state_vector_3;
        state_vector_3 = temp_vector;
    }

    // 逆序
    uint32_t output_buffer[16];
    _mm256_store_si256((__m256i*)(output_buffer), state_vector_3);
    _mm256_store_si256((__m256i*)(output_buffer + 8), state_vector_2);

    for (int i = 0; i < 4; i++) {
        memcpy(output_blocks[i], &output_buffer[i * 4], 16);
    }
}

